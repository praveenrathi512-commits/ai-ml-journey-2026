# Number Checker

number = 7

if number > 0:
  print("Positive Number")
elif number == 0:
  print("Zero")
else:
  print("Negative Number")


# Marks grading system

marks = 90

if marks >= 90:
  print("Grade A")
elif marks >= 75:
  print("Grade B")
elif marks >= 60:
  print("Grade C")
else:
  print("Fail")